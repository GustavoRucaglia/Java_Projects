package classes;

public class Balao {
	
	private Integer id;
	private Boolean regularidade;
	private Integer qtdpessoa;
	private  Boolean piloto;
	private Integer valor = 500;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Boolean getRegularidade() {
		return regularidade;
	}
	public void setRegularidade(Boolean regularidade) {
		this.regularidade = regularidade;
	}
	public Integer getQtdpessoa() {
		return qtdpessoa;
	}
	public void setQtdpessoa(Integer qtdpessoa) {
		this.qtdpessoa = qtdpessoa;
	}
	public Boolean getPiloto() {
		return piloto;
	}
	public void setPiloto(Boolean piloto) {
		this.piloto = piloto;
	}
	public Integer getValor() {
		return valor;
	}
	
	
	
}
